#ifndef _TYPES_H_
#define _TYPES_H_

// Just easier to read with the types redefined as follows.
//		Note, this emulator assumes that these types are actually
//		what the new type names suggest they are ... 

typedef unsigned char uint8_t;
typedef unsigned short uint16_t;
typedef unsigned int uint32_t;

typedef signed char int8_t;
typedef signed short int16_t;
typedef signed int int32_t;

#endif 